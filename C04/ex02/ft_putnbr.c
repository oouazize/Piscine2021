#include <unistd.h>
void	ft_putnbr(int nb)
{
	char	chr;

	if (nb < 0)
	{
		if (nb == -2147483648)
		{
			write(1, "-2147483648", 11);
			return ;
		}
		nb *= -1;
		write(1, "-", 1);
	}
	chr = nb % 10 + '0';
	if (nb >= 10)
		ft_putnbr(nb / 10);
	write(1, &chr, 1);
}
